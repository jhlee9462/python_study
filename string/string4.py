myString = 'Monty Python'
print(myString[0:4])
# Mont 가 출력됩니다. 여기서 0 to 4 에서 4 에 대한 인덱스는 출력되는 값에 포함되지 않는 것을 확인하여야 합니다.
print(myString[6:7])
# P가 출력됩니다.
print(myString[6:20])
# Python 이 출력됩니다.
print(myString[:2])
# index 값이 2 에 해당하는 문자 앞부터 출력됩니다.
print(myString[8:])
# index 값이 8 에 해당하는 문자부터 출력됩니다.
print(myString[:])
# 전체가 출력됩니다.